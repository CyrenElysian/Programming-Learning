#include <iostream>
using namespace std;

const int N=1e4+10;
int V[N],W[N];
int dp[N]; // dp[i]表示背包体积为i时，价值的最大值

// V容积、体积，W权重、价值
int main()
{
    int n,v; // 定义物体数量，背包容量
    cin>>n>>v;
    for(int i=1;i<=n;i++)
    {
        cin>>V[i]>>W[i]; // 输入第i物品的体积和价值
    }

    // j初始化为v(容量)，内层循环采用逆序遍历背包容量（从大到小）是为了确保每个物品只被考虑一次
    for(int i=1;i<=n;i++) // i表示轮数，第i轮表示处理第i个物品
    {
        for(int j=v;j>=V[i];j--)
        {
            dp[j]=max(dp[j],dp[j-V[i]]+W[i]);
        }
    }
    cout<<dp[v]<<endl;

    return 0;
}

// 在0-1背包问题中，每个物品最多只能选一次，因此我们需要保证在更新 dp[j] 时，用到的子问题 dp[j - V[i]] 是未考虑当前物品时的状态（即上一轮的结果）
// 如果内层循环正序遍历，那么当 j 从小到大更新时，dp[j - V[i]] 可能已经被当前物品更新过，这就相当于同一个物品被多次选取，违背了0-1背包的约束
// 而逆序遍历则保证了在更新 dp[j] 时，dp[j - V[i]] 还保留着上一轮的值，从而每个物品只被考虑一次

//    假设物品1：体积V[1] = 2，价值W[1] = 3；背包容量v = 4

//    顺序遍历（错误）：
//    初始化：dp = [0, 0, 0, 0, 0]。  dp[i]表示体积为i时的最大价值
//    处理物品1时，内层循环j从2递增到4：
//    j=2：dp[2] = max(0, dp[0] + 3) = 3（更新）
//    j=3：dp[3] = max(0, dp[1] + 3) = 3
//    j=4：dp[4] = max(0, dp[2] + 3) = max(0, 3 + 3) = 6（这里dp[2]已被更新为3，包含物品1，导致物品1被重复使用

//    逆序遍历（正确）：
//    初始化：dp = [0, 0, 0, 0, 0]（容量0到4）
//    处理物品1时，内层循环j从4递减到2：
//    j=4：dp[4] = max(dp[4], dp[4-2] + 3) = max(0, dp[2] + 3) = max(0, 0 + 3) = 3
//    j=3：dp[3] = max(dp[3], dp[3-2] + 3) = max(0, dp[1] + 3) = max(0, 0 + 3) = 3
//    j=2：dp[2] = max(dp[2], dp[2-2] + 3) = max(0, dp[0] + 3) = 3